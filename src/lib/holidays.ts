export function isBusinessDay(dateStr: string): boolean {
  const date = new Date(dateStr)
  const day = date.getUTCDay() // 0=일, 6=토
  if (day === 0 || day === 6) return false
  if (HOLIDAYS.has(dateStr)) return false
  return true
}
